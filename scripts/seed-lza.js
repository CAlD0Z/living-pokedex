async function run() {
  const client = await pool.connect();
  try {
    await client.query('BEGIN');

    // ── Insert Legends: Z-A game ──────────────────────────────────────────
    const existing = await client.query(
      "SELECT id FROM games WHERE name = 'Legends: Z-A'"
    );
    if (existing.rows.length === 0) {
      await client.query(
        "INSERT INTO games (name, game_group, generation, sort_order) VALUES ('Legends: Z-A', 'LZA', 9, 0)"
      );
      console.log('Inserted game: Legends: Z-A');
    } else {
      console.log('Game already exists: Legends: Z-A');
    }

    // ── Seed dex entries ─────────────────────────────────────────────────
    const dexes = [
      { key: 'lumiose',        entries: LUMIOSE    },
      { key: 'hyperspace',     entries: HYPERSPACE  },
      { key: 'mega-evolution', entries: MEGA_EVO   },
    ];

    // Verify all pokemon_ids exist
    const allIds = [...new Set([...LUMIOSE, ...HYPERSPACE, ...MEGA_EVO].map(([, id]) => id))];
    const { rows: found } = await client.query(
      'SELECT id FROM pokedex WHERE id = ANY($1)', [allIds]
    );
    const foundSet = new Set(found.map(r => r.id));
    const missing = allIds.filter(id => !foundSet.has(id));
    if (missing.length) {
      console.warn('WARNING: missing pokemon ids (will skip):', missing);
    }

    for (const { key, entries } of dexes) {
      await client.query('DELETE FROM dex_entries WHERE dex_key = $1', [key]);
      let inserted = 0;
      for (const [regional_number, pokemon_id] of entries) {
        if (!foundSet.has(pokemon_id)) continue;
        await client.query(
          'INSERT INTO dex_entries (dex_key, regional_number, pokemon_id) VALUES ($1, $2, $3)',
          [key, regional_number, pokemon_id]
        );
        inserted++;
      }
      console.log(`Seeded dex '${key}': ${inserted} entries`);
    }

    await client.query('COMMIT');
    console.log('Done.');
  } catch (err) {
    await client.query('ROLLBACK');
    throw err;
  } finally {
    client.release();
    await pool.end();
  }
}

run().catch(err => { console.error(err); process.exit(1); });
